        public static string ConvertLineEmoji(int emoji) {
            var codes = new int[] { emoji };
            var sb = new StringBuilder(codes.Length);
            foreach (var code in codes)
                sb.Append(Char.ConvertFromUtf32(code));

            return sb.ToString();
        }
